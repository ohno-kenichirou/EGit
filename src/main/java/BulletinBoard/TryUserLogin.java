package BulletinBoard;

public class TryUserLogin {

	private String email;
	private String pass;
	
	public TryUserLogin(String email, String pass) {
		this.email = email;
		this.pass = pass;
	}

	public String getEmail() {
		return email;
	}

	public String getPass() {
		return pass;
	}
	
	
	
}
